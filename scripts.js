function brightnessOn() {
    newmain = document.getElementById('main');
    newmain.style.cssText = 'background: rgb(0, 0, 0, .8); transition: .5s'
}

function brightnessOff() {
    newmain = document.getElementById('main');
    newmain.style.cssText = 'background: rgb(0, 0, 0, 0); transition: .5s'
}


